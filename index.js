
let num1 = parseInt(prompt("Number1: "));
let opr = prompt("Operator: +, -, *, /");
let num2 = parseInt(prompt("Number2: "));

if (opr == "+") {
  alert(num1 + num2);
} else if (opr == "-") {
  alert(num1 - num2);
} else if (opr == "*") {
  alert(num1 * num2);
} else if (opr == "/") {
  alert(num1 / num2);
}


